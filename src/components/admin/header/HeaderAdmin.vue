import SockJS from 'sockjs-client';
import Stomp from 'webstomp-client';
export default {

    data(){
        return{
            chatBoxList: []
        }
    },

    methods: {

        connect(){
            try{    
                this.socket = new SockJS(process.env.VUE_APP_WEBSOCKET_URL);
                this.stompClient = Stomp.over(this.socket)
                this.stompClient.connect(
                    {},
                    frame => {
                        console.log("socket connect success "+frame);
                        this.stompClient.subscribe('/topic/all/chatrooms', this.reloadChatBox);
                    },
                    error => {
                       console.log(error);
                       this.disconnect();
                    }
                );
            }catch(err){
                console.log("err socket: "+err)
            }
        },

        disconnect() {
            if (this.stompClient && this.stompClient.connected) {
               document.getElementById('chatbox-messages').innerHTML = '';
               this.stompClient.disconnect();
               console.log("socket disconnect")
            }
        },

        async getChatBoxList(){
            var resp = await this.$httpClient.get("/chatroom/findByAdmin", true, {})
            if(!resp.result){
                return this.showErrorMsg(resp.message)
            }
            this.chatBoxList = resp.data
        },

        reloadChatBox(message){
            this.getChatBoxList();
            this.selectChatBox(message.body);
        },

        selectChatBox(chatRoomId){
            this.emitter.emit('openChatBox', {chatRoomId: chatRoomId})
        },

        logout(){
            this.$store.commit("authen/LOGOUT");
            window.location.href = "/";
        }
    },

    beforeMount(){
        this.getChatBoxList();
        this.connect();
    }

    
}
